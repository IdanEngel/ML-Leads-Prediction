from sqlalchemy import Column, Integer, String, Float
from sqlalchemy.ext.declarative import declarative_base

Base = declarative_base()

class LeadRecord(Base):
    __tablename__ = "leads"

    id = Column(Integer, primary_key=True, index=True)
    Lead_Number = Column(Integer)
    Lead_Origin = Column(String)
    Lead_Source = Column(String)
    Do_Not_Email = Column(String)
    Do_Not_Call = Column(String)
    TotalVisits = Column(Float)
    Total_Time_Spent_on_Website = Column(Float)
    Page_Views_Per_Visit = Column(Float)
    Last_Activity = Column(String)
    Country = Column(String)
    Specialization = Column(String)
    How_did_you_hear_about_X_Education = Column(String)
    What_is_your_current_occupation = Column(String)
    What_matters_most_to_you_in_choosing_a_course = Column(String)
    Search = Column(String)
    Magazine = Column(String)
    Newspaper_Article = Column(String)
    X_Education_Forums = Column(String)
    Newspaper = Column(String)
    Digital_Advertisement = Column(String)
    Through_Recommendations = Column(String)
    Receive_More_Updates_About_Our_Courses = Column(String)
    Tags = Column(String)
    Lead_Quality = Column(String)
    Update_me_on_Supply_Chain_Content = Column(String)
    Get_updates_on_DM_Content = Column(String)
    Lead_Profile = Column(String)
    City = Column(String)
    Asymmetrique_Activity_Index = Column(String)
    Asymmetrique_Profile_Index = Column(String)
    Asymmetrique_Activity_Score = Column(Float)
    Asymmetrique_Profile_Score = Column(Float)
    I_agree_to_pay_the_amount_through_cheque = Column(String)
    A_free_copy_of_Mastering_The_Interview = Column(String)
    Last_Notable_Activity = Column(String)
    score = Column(Float)